package ravensproject.transformationTables;

public class AngleSet {
	
	private double ang1;
	private double ang2;
	
	public AngleSet(double a, double b){
		this.ang1 = a;
		this.ang2 = b;
	}

	public double getAng1() {
		return ang1;
	}

	public void setAng1(double ang1) {
		this.ang1 = ang1;
	}

	public double getAng2() {
		return ang2;
	}

	public void setAng2(double ang2) {
		this.ang2 = ang2;
	}
	

}
